package colecoes;

import java.util.HashSet;
import java.util.Set;

public class ConjuntoBaguncado {
	
	@SuppressWarnings("unchecked")
	public static void main(String[] args) {
		
		@SuppressWarnings("rawtypes")
		HashSet conjunto = new HashSet();
		
		conjunto.add(1.2); //conversao implicita pra uma classe do tipo Double.
		conjunto.add(true); //boolean para Boolean.
		conjunto.add("Teste");
		conjunto.add(1);//int para Integer
		conjunto.add('x');
		conjunto.add('y');
			
		System.out.println("O tamanho do conjunto é "+ conjunto.size());
		System.out.println(conjunto);

		conjunto.remove('y');
		System.out.println("O tamanho do conjunto é "+ conjunto.size());
		System.out.println(conjunto);
		
		System.out.println(conjunto.contains('x'));
		System.out.println(conjunto.contains('z'));
		
		Set nums = new HashSet();
		nums.add(1);
		nums.add(2);
		nums.add(3);
		
		
		conjunto.retainAll(nums);//union
		System.out.println(conjunto);

		conjunto.addAll(nums);
		System.out.println(conjunto);

		conjunto.clear();
		System.out.println(conjunto);
		
	}

}
